CREATE TABLE STOCK_ITEM_QUANTITY_HISTORY(
	ID INTEGER GENERATED BY DEFAULT AS IDENTITY(START WITH 100, INCREMENT BY 1) PRIMARY KEY,
	STOCK_ITEM_ID				INTEGER,
	OLDQUANTITY					VARCHAR(10) DEFAULT NULL,
	PLUSQUANTITY				VARCHAR(10) DEFAULT NULL,
	MINUSQUANTITY				VARCHAR(10) DEFAULT NULL,
	NEWQUANTITY					VARCHAR(10) DEFAULT NULL,
	ACTION						VARCHAR(8) DEFAULT NULL,
	DESCRIPTION					VARCHAR(100) DEFAULT NULL,
	CREATEDDATE					TIMESTAMP,
	LASTUPDATEDDATE				TIMESTAMP,
	ISENABLE					BOOLEAN DEFAULT TRUE
);
COMMIT;
