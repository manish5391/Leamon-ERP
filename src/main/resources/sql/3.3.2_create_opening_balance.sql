CREATE TABLE OPENING_BALANCE_INFO(
	ID INTEGER GENERATED BY DEFAULT AS IDENTITY(START WITH 100, INCREMENT BY 1) PRIMARY KEY,
	PARTYINFOID						VARCHAR(100) DEFAULT NULL,
	BILLNUMBER						VARCHAR(100) DEFAULT NULL,
	BILLDATE						VARCHAR(100) DEFAULT NULL,	
	OPENINGBALANCEAMOUNT			VARCHAR(100) DEFAULT NULL,
	RECEIVEDOPENINGBALANCEAMOUNT	VARCHAR(100) DEFAULT NULL,
	REMAININGOPENINGBALANCEAMOUNT	VARCHAR(100) DEFAULT NULL,
	REMARK							VARCHAR(500) DEFAULT NULL,
	TYPE							VARCHAR(100) DEFAULT NULL,  --W-Without Bill, B- With Bill
	STATUS							VARCHAR(100) DEFAULT NULL,
	CREATEDDATE						TIMESTAMP,
	LASTUPDATED						TIMESTAMP,
	ISENABLE						BOOLEAN DEFAULT TRUE
);